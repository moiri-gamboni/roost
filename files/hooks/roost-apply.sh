#!/bin/bash
# Deploy config files and reload affected services.
#
# Subcommand mode (manifest-based file deployment):
#   roost-apply [diff|push|list] [file-filter...]
#
# Flag mode (direct service reload):
#   roost-apply [--all] [--cloudflare] [--caddy] [--ntfy] [--systemd] [--cron]
#
# Options:
#   -y, --yes    Skip confirmation prompts (push only)
set -euo pipefail
source "$(dirname "$(readlink -f "$0")")/_hook-env.sh"

# --- Paths ---
_HOOKS_DIR="$(cd "$(dirname "$(readlink -f "$0")")" && pwd)"
_ROOST_DIR="$(cd "$_HOOKS_DIR/../.." && pwd)"
REPO_DIR="$_ROOST_DIR/code/server"

# --- Output helpers ---
info() { echo "  [*] $1"; }
ok()   { echo "  [+] $1"; }
skip() { echo "  [-] $1 (skipped)"; }
warn() { echo "  [!] $1"; }

# ============================================
# Environment
# ============================================

source_env() {
    local sync_env="$_ROOST_DIR/.sync-env"
    [ -f "$sync_env" ] || regenerate_sync_env "$sync_env"
    source "$sync_env"
    ROOST_DIR_NAME="${ROOST_DIR_NAME:?ROOST_DIR_NAME not set}"
    TUNNEL_NAME="${CLOUDFLARE_TUNNEL_NAME:-$ROOST_DIR_NAME}"
    HOME_DIR="/home/$USERNAME"
    ROOST_DIR="$HOME_DIR/$ROOST_DIR_NAME"
}

# Regenerate .sync-env from derivable values. deploy.sh will overwrite on next run.
regenerate_sync_env() {
    local sync_env="$1"
    local dir_name; dir_name=$(basename "$_ROOST_DIR")
    local owner; owner=$(stat -c '%U' "$_ROOST_DIR")
    local host; host=$(hostname)
    warn "$sync_env missing; regenerating from derived values"
    cat > "$sync_env" <<EOF
# Auto-regenerated by roost-apply on $(date -Iseconds).
# deploy.sh will overwrite on next deploy. DOMAIN left empty; set via .env + deploy.sh.
SERVER_NAME="$host"
USERNAME="$owner"
DOMAIN=""
ROOST_DIR_NAME="$dir_name"
CLOUDFLARE_TUNNEL_NAME="$dir_name"
EOF
    logger -t roost/roost-apply "Regenerated $sync_env"
}

# --- Dynamic variables (lazy-cached) ---
_RESOLVED_TAILSCALE_IP=""
_RESOLVED_TUNNEL_ID=""

tailscale_ip() {
    if [ -z "$_RESOLVED_TAILSCALE_IP" ]; then
        _RESOLVED_TAILSCALE_IP=$(tailscale ip -4 2>/dev/null || true)
        if [ -z "$_RESOLVED_TAILSCALE_IP" ]; then
            warn "Could not resolve Tailscale IP"
        fi
    fi
    echo "$_RESOLVED_TAILSCALE_IP"
}

tunnel_id() {
    if [ -z "$_RESOLVED_TUNNEL_ID" ]; then
        # .base is the source of truth; .yml is the assembled output.
        # Fall back to .yml on pre-migration servers.
        for _f in /etc/cloudflared/config.yml.base /etc/cloudflared/config.yml; do
            _RESOLVED_TUNNEL_ID=$(sudo grep -oP '^tunnel: \K.+' "$_f" 2>/dev/null || true)
            [ -n "$_RESOLVED_TUNNEL_ID" ] && break
        done
        if [ -z "$_RESOLVED_TUNNEL_ID" ]; then
            warn "Could not resolve TUNNEL_ID from /etc/cloudflared/config.yml{,.base}"
        fi
    fi
    echo "$_RESOLVED_TUNNEL_ID"
}

