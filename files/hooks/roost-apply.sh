# Expand variables in server path.
# $ROOST_DIR_NAME must be expanded before $ROOST_DIR to avoid partial matching.
expand_path() {
    local path="$1"
    path="${path//\$ROOST_DIR_NAME/$ROOST_DIR_NAME}"
    path="${path//\$HOME_DIR/$HOME_DIR}"
    path="${path//\$ROOST_DIR/$ROOST_DIR}"
    path="${path//\$USERNAME/$USERNAME}"
    echo "$path"
}

# Check if a file needs root to write
needs_root() {
    # Assume root ownership for any absolute path outside /home and /tmp.
    # Previously only matched /etc/*, which broke deploys to /usr/local/bin/*
    # and similar root-owned paths (e.g. proton-keepalive-check after it had
    # already been installed via sudo by setup/travel-vpn.sh).
    case "$1" in
        /home/*|/tmp/*) return 1 ;;
        /*) return 0 ;;
        *) return 1 ;;
    esac
}

# ============================================
# Transforms
# ============================================

# Render a repo file with its transform applied
render_file() {
    local repo_path="$1"
    local transform="$2"
    local full_path="$REPO_DIR/$repo_path"

    if [ ! -f "$full_path" ]; then
        return 1
    fi

    case "$transform" in
        plain|plain+x)
            cat "$full_path"
            ;;
        sed-roost)
            sed "s|~/roost/|~/$ROOST_DIR_NAME/|g" "$full_path"
            ;;
        envsubst:*)
            local vars_csv="${transform#envsubst:}"
            local var_list=""
            IFS=',' read -ra var_names <<< "$vars_csv"
            for v in "${var_names[@]}"; do
                var_list+=" \$$v"
            done

            # Export required variables for envsubst
            export TUNNEL_ID
            TUNNEL_ID=$(tunnel_id)
            export TAILSCALE_IP
            TAILSCALE_IP=$(tailscale_ip)
            export TUNNEL_NAME
            export USERNAME
            export HOME_DIR
            export ROOST_DIR_NAME
            export DOMAIN
            export HETZNER_PUBLIC_IPV4
            export HETZNER_PUBLIC_IPV6

            # Guard: abort if any required variable is empty
            for v in "${var_names[@]}"; do
                if [ -z "${!v:-}" ]; then
                    echo "Error: $v is empty, refusing to render $full_path" >&2
                    return 1
                fi
            done

            envsubst "$var_list" < "$full_path"
            ;;
        *)
            echo "Error: Unknown transform '$transform'" >&2
            return 1
            ;;
    esac
}

# ============================================
# Manifest Filtering
# ============================================

get_manifest() {
    local line
    while IFS= read -r line; do
        [ -z "$line" ] && continue
        local repo_path="${line%%|*}"

        if [ ${#FILE_FILTERS[@]} -gt 0 ]; then
            local match=false
            for filter in "${FILE_FILTERS[@]}"; do
                if [[ "$repo_path" == *"$filter"* ]] || [[ "$filter" == *"$repo_path"* ]]; then
                    match=true
                    break
                fi
            done
            [ "$match" = false ] && continue
        fi

        echo "$line"
    done <<< "$(define_manifest)"
}

# Read deployed file content, using sudo for root-owned paths
read_deployed() {
    local server_path="$1"
    if needs_root "$server_path"; then
        sudo cat "$server_path" 2>/dev/null || true
    else
        cat "$server_path" 2>/dev/null || true
    fi
}

# Check if deployed file exists, using sudo for root-owned paths
deployed_exists() {
    local server_path="$1"
    if needs_root "$server_path"; then
        sudo test -f "$server_path"
    else
        test -f "$server_path"
    fi
}

# ============================================
# Subcommand: list
# ============================================

cmd_list() {
    source_env
    echo ""
    echo "  Managed files:"
    echo ""

    printf "  %-45s  %-50s  %s\n" "REPO PATH" "DEPLOYED PATH" "TRANSFORM"
    printf "  %-45s  %-50s  %s\n" "---------" "-------------" "---------"

    while IFS='|' read -r repo_path server_path transform service_action; do
        server_path=$(expand_path "$server_path")
        printf "  %-45s  %-50s  %s\n" "$repo_path" "$server_path" "$transform"
    done <<< "$(get_manifest)"
    echo ""
}

# ============================================
# Subcommand: diff
# ============================================

cmd_diff() {
    source_env
    local has_diff=false
    local missing_local=()
    local missing_dest=()

    while IFS='|' read -r -u3 repo_path server_path transform service_action; do
        server_path=$(expand_path "$server_path")
        local full_repo_path="$REPO_DIR/$repo_path"

        if [ ! -f "$full_repo_path" ]; then
            missing_local+=("$repo_path")
            continue
        fi

        local local_content
        local_content=$(render_file "$repo_path" "$transform") || { warn "Failed to render $repo_path, skipping"; continue; }

        if ! deployed_exists "$server_path"; then
            missing_dest+=("$repo_path -> $server_path")
            continue
        fi

        local dest_content
        dest_content=$(read_deployed "$server_path")

        local diff_output
        diff_output=$(diff -u \
            --label "deployed:$server_path" \
            --label "repo:$repo_path (rendered)" \
            <(echo "$dest_content") \
            <(echo "$local_content") \
        ) || true

        if [ -n "$diff_output" ]; then
            has_diff=true
            echo ""
            echo "--- $repo_path -> $server_path [$transform]"
            echo "$diff_output"
        fi
    done 3<<< "$(get_manifest)"

    if [ ${#missing_local[@]} -gt 0 ]; then
        echo ""
        warn "Local files not found (not yet created?):"
        for f in "${missing_local[@]}"; do
            echo "    $f"
        done
    fi

    if [ ${#missing_dest[@]} -gt 0 ]; then
        echo ""
        warn "Deployed files not found (not yet deployed?):"
        for f in "${missing_dest[@]}"; do
            echo "    $f"
        done
    fi

    if [ "$has_diff" = false ] && [ ${#missing_local[@]} -eq 0 ] && [ ${#missing_dest[@]} -eq 0 ]; then
        echo ""
        ok "All files in sync"
    fi
    echo ""
}

# ============================================
# Subcommand: push
# ============================================

cmd_push() {
    source_env
    logger -t "$_HOOK_TAG" "Push started"

    # Phase 1: Compute diffs and collect changes
    local -a changed_repos=()
    local -a changed_servers=()
    local -a changed_transforms=()
    local -a changed_services=()
    local -a changed_contents=()
    local -a diff_outputs=()

    while IFS='|' read -r -u3 repo_path server_path transform service_action; do
        server_path=$(expand_path "$server_path")
        local full_repo_path="$REPO_DIR/$repo_path"

        if [ ! -f "$full_repo_path" ]; then
            skip "$repo_path (local file missing)"
            continue
        fi

        local local_content
        local_content=$(render_file "$repo_path" "$transform") || { warn "Failed to render $repo_path, skipping"; continue; }

        local dest_content
        dest_content=$(read_deployed "$server_path")

        local diff_output
        diff_output=$(diff -u \
            --label "deployed:$server_path" \
            --label "repo:$repo_path (rendered)" \
            <(echo "$dest_content") \
            <(echo "$local_content") \
        ) || true

        if [ -n "$diff_output" ]; then
            changed_repos+=("$repo_path")
            changed_servers+=("$server_path")
            changed_transforms+=("$transform")
            changed_services+=("$service_action")
            changed_contents+=("$local_content")
            diff_outputs+=("$diff_output")
        fi
    done 3<<< "$(get_manifest)"

    if [ ${#changed_repos[@]} -eq 0 ]; then
        ok "Nothing to push (all files in sync)"
        return 0
    fi

    # Phase 2: Show preview
    echo ""
    echo "  Changes to push:"
    echo ""
    for i in "${!changed_repos[@]}"; do
        echo "--- ${changed_repos[$i]} -> ${changed_servers[$i]} [${changed_transforms[$i]}]"
        echo "${diff_outputs[$i]}"
        echo ""
    done

    # Phase 3: Confirm
    if [ "$AUTO_CONFIRM" = false ]; then
        echo "  ${#changed_repos[@]} file(s) will be updated."
        read -rp "  Push? [y/N] " confirm
        case "$confirm" in
            [yY]|[yY][eE][sS]) ;;
            *)
                echo "  Aborted."
                return 0
                ;;
        esac
    fi

    # Phase 4: Write files
    local need_daemon_reload=false
    local -A services_to_restart=()
    local -a run_commands=()

    echo ""
    for i in "${!changed_repos[@]}"; do
        local sp="${changed_servers[$i]}"
        local transform="${changed_transforms[$i]}"
        local content="${changed_contents[$i]}"
        local service="${changed_services[$i]}"

        # Ensure parent directory exists and write file
        local parent_dir
        parent_dir=$(dirname "$sp")
        if needs_root "$sp"; then
            sudo mkdir -p "$parent_dir"
            printf '%s\n' "$content" | sudo tee "$sp" > /dev/null
            # sudo tee preserves existing non-root ownership; enforce root:root 0644 for /etc/*
            sudo chown root:root "$sp"
            sudo chmod 644 "$sp"
        else
            mkdir -p "$parent_dir"
            printf '%s\n' "$content" > "$sp"
        fi

        # Set executable if needed
        if [[ "$transform" == *"+x"* ]]; then
            if needs_root "$sp"; then
                sudo chmod +x "$sp"
            else
                chmod +x "$sp"
            fi
        fi

        # Track service actions
        IFS=',' read -ra actions <<< "$service"
        for action in "${actions[@]}"; do
            action="${action# }"; action="${action% }"
            [ -z "$action" ] && continue
            case "$action" in
                daemon-reload)
                    need_daemon_reload=true
                    ;;
                run:*)
                    run_commands+=("${action#run:}")
                    ;;
                *)
                    services_to_restart["$action"]=1
                    ;;
            esac
        done

        ok "${changed_repos[$i]} -> $sp"
    done

    # Phase 5: daemon-reload → run commands → restart services.
    # Run commands fire before restarts so post-write transforms (e.g.
    # cloudflare-assemble.sh building config.yml from config.yml.base +
    # fragments) land before the service picks up its new config.
    if [ "$need_daemon_reload" = true ]; then
        info "Running systemctl daemon-reload..."
        sudo systemctl daemon-reload
        ok "daemon-reload"
    fi

    # Run commands are from the hardcoded manifest only (no user input)
    for cmd in "${run_commands[@]}"; do
        info "Running: $cmd"
        bash -c "$cmd" || true
    done

    local restart_failed=()
    for action in "${!services_to_restart[@]}"; do
        local verb="${action%%:*}"
        local unit="${action#*:}"
        info "Running systemctl $verb $unit..."
        if sudo systemctl "$verb" "$unit"; then
            ok "$verb $unit"
        else
            restart_failed+=("$unit")
            warn "Failed: systemctl $verb $unit"
        fi
    done

    echo ""
    ok "Push complete (${#changed_repos[@]} file(s) updated)"
    logger -t "$_HOOK_TAG" "Push complete: ${#changed_repos[@]} file(s) updated"

    # Notify on failures
    if [ ${#restart_failed[@]} -gt 0 ]; then
        ntfy_send -t "roost-apply: restart failures" -p "high" \
            "Failed to restart: ${restart_failed[*]}"
    fi
}

# ============================================
# Flag mode: direct service reload
# ============================================

cmd_flag_reload() {
    source_env
    logger -t "$_HOOK_TAG" "Flag reload started"

    local reload_failed=()

    if $FLAG_ALL || $FLAG_CLOUDFLARE; then
        if [ -x "$_HOOKS_DIR/cloudflare-assemble.sh" ]; then
            info "Running cloudflare assembly..."
            "$_HOOKS_DIR/cloudflare-assemble.sh"
            ok "Cloudflare config assembled"
        fi
        info "Restarting cloudflared..."
        if sudo systemctl restart cloudflared; then
            ok "cloudflared restarted"
        else
            reload_failed+=("cloudflared")
            warn "Failed: systemctl restart cloudflared"
        fi
    fi

    if $FLAG_ALL || $FLAG_SYSTEMD; then
        info "Running systemctl daemon-reload..."
        sudo systemctl daemon-reload
        ok "daemon-reload"
    fi

    if $FLAG_ALL || $FLAG_CADDY; then
        info "Reloading Caddy..."
        if sudo systemctl reload-or-restart caddy; then
            ok "Caddy reloaded"
        else
            reload_failed+=("caddy")
            warn "Failed: systemctl reload-or-restart caddy"
        fi
    fi

    if $FLAG_ALL || $FLAG_NTFY; then
        info "Restarting ntfy..."
        if sudo systemctl restart ntfy; then
            ok "ntfy restarted"
        else
            reload_failed+=("ntfy")
            warn "Failed: systemctl restart ntfy"
        fi
    fi

    if $FLAG_ALL || $FLAG_SYSTEMD; then
        for unit in glances ram-monitor.timer; do
            info "Restarting $unit..."
            if sudo systemctl restart "$unit"; then
                ok "$unit restarted"
            else
                reload_failed+=("$unit")
                warn "Failed: systemctl restart $unit"
            fi
        done
    fi

    if $FLAG_ALL || $FLAG_CRON; then
        ok "Cron (auto-reloads, no action needed)"
    fi

    if $FLAG_ALL || $FLAG_XRAY; then
        local template="$REPO_DIR/files/travel/xray-config.json.tmpl"
        local state="/etc/roost-travel/state.env"
        if [ ! -f "$template" ]; then
            warn "Missing Xray template: $template"
        elif ! sudo test -r "$state"; then
            warn "Missing state.env: $state (run setup/travel-vpn.sh or keys-init.sh first)"
        else
            info "Re-rendering /etc/xray/config.json..."
            local tmp
            # .json suffix so `xray run -test` autodetects the format.
            tmp=$(mktemp --suffix=.json)
            set -a
            # shellcheck disable=SC1090
            eval "$(sudo cat "$state")"
            set +a
            # Sourced helper defines $XRAY_ENVSUBST_VARS — kept identical with
            # files/setup/travel-vpn.sh's render path to avoid silent allowlist drift.
            # shellcheck disable=SC1091
            source "$REPO_DIR/files/travel/_envsubst-vars.sh"
            envsubst "$XRAY_ENVSUBST_VARS" \
                < "$template" > "$tmp"
            # Validate before install: a parse-time error (broken JSON, wrong
            # types, missing required fields) would otherwise install
            # successfully and crash xray on the subsequent restart, taking
            # down all paths. `xray run -test` catches that class. Note: xray
            # accepts unknown fields silently, so this guard does NOT catch
            # the field-name-typo class (use upstream docs to verify).
            if ! sudo /usr/local/bin/xray run -test -c "$tmp" >/dev/null 2>&1; then
                local err
                err=$(sudo /usr/local/bin/xray run -test -c "$tmp" 2>&1 || true)
                warn "xray config validation failed; keeping previous /etc/xray/config.json"
                echo "$err" | sed 's/^/    /' >&2
                ntfy_send -t "roost-apply: xray config invalid" -p "high" \
                    "Rendered xray config failed 'xray run -test'; previous config kept. Check journalctl -t roost/roost-apply." \
                    || true
                rm -f "$tmp"
                reload_failed+=("xray")
            else
                sudo install -m 0640 -o root -g xray "$tmp" /etc/xray/config.json
                rm -f "$tmp"
                ok "Xray config rendered"
                info "Restarting xray..."
                if sudo systemctl restart xray; then
                    ok "xray restarted"
                else
                    reload_failed+=("xray")
                    warn "Failed: systemctl restart xray"
                fi
            fi
        fi
    fi

    if $FLAG_ALL || $FLAG_PROTON; then
        info "Running systemctl daemon-reload for Proton units..."
        sudo systemctl daemon-reload
        ok "daemon-reload"
        if sudo systemctl is-active proton-keepalive.timer >/dev/null; then
            info "Restarting proton-keepalive.timer..."
            if sudo systemctl restart proton-keepalive.timer; then
                ok "proton-keepalive.timer restarted"
            else
                reload_failed+=("proton-keepalive.timer")
            fi
        else
            skip "proton-keepalive.timer inactive (vpn=off)"
        fi
        if sudo systemctl is-active proton-routing-ensure.timer >/dev/null; then
            info "Restarting proton-routing-ensure.timer..."
            if sudo systemctl restart proton-routing-ensure.timer; then
                ok "proton-routing-ensure.timer restarted"
            else
                reload_failed+=("proton-routing-ensure.timer")
            fi
        else
            skip "proton-routing-ensure.timer inactive (vpn=off)"
        fi
        # is-active distinguishes active-since-cmd_vpn-on from a lingering DOWN
        # interface: restarting the unit on the latter would flip VPN on without
        # going through cmd_vpn's ASN gate or state-file update.
        if sudo systemctl is-active --quiet wg-quick@wg-proton; then
            info "Restarting wg-quick@wg-proton to pick up drop-in..."
            if sudo systemctl restart wg-quick@wg-proton; then
                ok "wg-quick@wg-proton restarted"
            else
                reload_failed+=("wg-quick@wg-proton")
            fi
        else
            skip "wg-quick@wg-proton inactive (vpn=off)"
        fi
    fi

    if [ ${#reload_failed[@]} -gt 0 ]; then
        ntfy_send -t "roost-apply: reload failures" -p "high" \
            "Failed to reload: ${reload_failed[*]}"
    fi

    logger -t "$_HOOK_TAG" "Flag reload complete"
}

# ============================================
# Argument Parsing
# ============================================

AUTO_CONFIRM=false
SUBCOMMAND=""
FILE_FILTERS=()
FLAG_ALL=false
FLAG_CLOUDFLARE=false
FLAG_CADDY=false
FLAG_NTFY=false
FLAG_SYSTEMD=false
FLAG_CRON=false
FLAG_XRAY=false
FLAG_PROTON=false
HAS_FLAGS=false

for arg in "$@"; do
    case "$arg" in
        -h|--help)
            cat <<'USAGE'
Usage: roost-apply [diff|push|list] [file-filter...]
       roost-apply [--all] [--cloudflare] [--caddy] [--ntfy] [--systemd] [--cron] [--xray] [--proton]

Subcommands (manifest-based file deployment):
  diff     Show differences between repo and deployed files (default)
  push     Deploy changed files (shows preview, confirms)
  list     List all managed files

Flags (direct service reload):
  --all        Reload all services
  --cloudflare Assemble fragments and restart cloudflared
  --caddy      Reload-or-restart Caddy
  --ntfy       Restart ntfy
  --systemd    Daemon-reload and restart systemd units
  --cron       No-op (cron auto-reloads)
  --xray       Re-render /etc/xray/config.json from state.env and restart xray
  --proton     Daemon-reload, restart proton-keepalive.timer + proton-routing-ensure.timer + wg-quick@wg-proton (if up)

Options:
  -y, --yes    Skip confirmation prompts (push only)
USAGE
            exit 0
            ;;
        -y|--yes) AUTO_CONFIRM=true ;;
        --all)        FLAG_ALL=true; HAS_FLAGS=true ;;
        --cloudflare) FLAG_CLOUDFLARE=true; HAS_FLAGS=true ;;
        --caddy)      FLAG_CADDY=true; HAS_FLAGS=true ;;
        --ntfy)       FLAG_NTFY=true; HAS_FLAGS=true ;;
        --systemd)    FLAG_SYSTEMD=true; HAS_FLAGS=true ;;
        --cron)       FLAG_CRON=true; HAS_FLAGS=true ;;
        --xray)       FLAG_XRAY=true; HAS_FLAGS=true ;;
        --proton)     FLAG_PROTON=true; HAS_FLAGS=true ;;
        diff|push|list)
            if [ -z "$SUBCOMMAND" ]; then
                SUBCOMMAND="$arg"
            else
                FILE_FILTERS+=("$arg")
            fi
            ;;
        -*)
            echo "Unknown flag: $arg" >&2
            exit 1
            ;;
        *) FILE_FILTERS+=("$arg") ;;
    esac
done

# Validate: subcommands and flags are mutually exclusive
if [ -n "$SUBCOMMAND" ] && [ "$HAS_FLAGS" = true ]; then
    echo "Error: subcommands (diff/push/list) and flags (--caddy etc.) are mutually exclusive" >&2
    exit 1
fi

# ============================================
# Dispatch
# ============================================

if [ "$HAS_FLAGS" = true ]; then
    cmd_flag_reload
else
    # Subcommand mode requires the repo
    if [ ! -d "$REPO_DIR/files" ]; then
        echo "Error: Repo not found at $REPO_DIR/" >&2
        echo "Expected the server repo at \$ROOST_DIR/code/server/" >&2
        exit 1
    fi
    case "${SUBCOMMAND:-diff}" in
        diff) cmd_diff ;;
        push) cmd_push ;;
        list) cmd_list ;;
    esac
fi
